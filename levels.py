class Level:
	def __init__(self):
		self.level_indx = 8
		#groups
		self.Gdrawables = pg.sprite.Group()
		self.Gdynamic = pg.sprite.Group()
		self.Grigid = pg.sprite.Group()
		self.Gground = pg.sprite.Group()
		self.Genemies = pg.sprite.Group()
		self.GmainNpc = pg.sprite.Group()
	
	def start(self):
		indx = self.level_indx
		#cleanup previous
		self.cleanup()
		
		#init level map
		self.level_map = None
		self.level_map = level_maps[indx]
		self.init_map()
		
		#init platforms
		self.platforms = None
		self.platforms = TwoPlatforms.create(platform_name[indx], self.Gdynamic)
		self.platforms.add((self.Gdrawables, self.Gdynamic, self.Gground))
		
		#init NPC
		t_npc = None
		t_npc = NPC.create(npc_name[indx], pos=npc_start_pos[indx], level=self)
		t_npc.add((self.Gdrawables, self.Gdynamic, self.Grigid, self.GmainNpc))
		
		#init enemies
		for (enemy_type, enemy_pos) in enemies[indx]:
			npc = NPC.create(enemy_type, enemy_pos)
			npc.add((self.Gdrawables, self.Gdynamic, self.Grigid, self.Genemies))
	
	def finish_level(self):
		print("level finished!")
		self.level_indx += 1
		self.start()
	
	def cleanup(self):
		self.Gdrawables.empty()
		self.Gdynamic.empty()
		self.Grigid.empty()
		self.Gground.empty()
	
	def init_map(self):
		for tile_y, tile_xl in enumerate(self.level_map):
			for tile_x, tile_type in enumerate(tile_xl):
				tile = Tile.create(tile_type, (tile_x, tile_y))
				if tile is not None:
					tile.add(self.Gdrawables, self.Gground)
					if isinstance(tile, JumpTile):
						tile.add(self.Gdynamic)
